package com.psg.jgringotts.dao;

public class JGringottsDAOException extends Exception {

    private static final long serialVersionUID = -7851739902384642707L;

    public JGringottsDAOException() {
        super();
    }

    public JGringottsDAOException(String message, Throwable cause) {
        super(message, cause);
    }

    public JGringottsDAOException(String message) {
        super(message);
    }

    public JGringottsDAOException(Throwable cause) {
        super(cause);
    }

}
